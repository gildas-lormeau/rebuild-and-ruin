import {
  type AutoResolveDeps,
  shouldAutoResolve,
  type UpgradePickDialogState,
  type UpgradePickEntry,
} from "../shared/dialog-types.ts";
import { GRID_COLS, GRID_ROWS } from "../shared/grid.ts";
import type { ValidPlayerSlot } from "../shared/player-slot.ts";
import { isGrass } from "../shared/spatial.ts";
import type { GameState } from "../shared/types.ts";
import { UID, type UpgradeId } from "../shared/upgrade-defs.ts";

interface CreateUpgradePickDeps extends AutoResolveDeps {
  readonly state: GameState;
}

const SMALL_PIECES_TERRITORY_RATIO = 0.8;
/** Auto-resolve delay before auto-picking (seconds). */
export const UPGRADE_PICK_AUTO_DELAY = 1.5;
/** Max time before force-picking for pending players (seconds). */
export const UPGRADE_PICK_MAX_TIMER = 15;

/** Create the pick dialog from pre-generated offers on GameState.
 *  Offers were generated by generateUpgradeOffers() in enterBuildFromBattle()
 *  using the synced RNG, so they're already deterministic.
 *  Returns null if picks are not applicable (classic mode, too early, no alive players). */
export function createUpgradePickDialog(
  deps: CreateUpgradePickDeps,
): UpgradePickDialogState | null {
  const { state } = deps;
  if (!state.modern?.pendingUpgradeOffers) return null;

  const entries: UpgradePickEntry[] = [];
  for (const [playerId, offers] of state.modern.pendingUpgradeOffers) {
    const autoResolve = shouldAutoResolve(playerId, deps);

    entries.push({
      playerId,
      offers,
      choice: null,
      autoResolve,
      autoTimer: 0,
      focusedCard: 0,
    });
  }

  if (entries.length === 0) return null;
  return { entries, timer: 0 };
}

/** Tick the upgrade pick dialog. Auto-resolve entries pick after a delay;
 *  max timer force-picks for all remaining entries.
 *  Returns true when all entries are resolved. */
// Parallel structure with tickLifeLostDialog (life-lost.ts) — both loop entries for auto-resolve + force-resolve.
export function tickUpgradePickDialog(
  dialog: UpgradePickDialogState,
  dt: number,
  autoDelay: number,
  maxTimer: number,
  state?: GameState,
): boolean {
  dialog.timer += dt;

  // Auto-resolve pending entries
  for (const entry of dialog.entries) {
    if (entry.choice !== null) continue;
    if (entry.autoResolve) {
      entry.autoTimer += dt;
      if (entry.autoTimer >= autoDelay) {
        entry.choice = state
          ? aiPickUpgrade(entry.offers, state, entry.playerId)
          : randomPickUpgrade(entry.offers);
      }
    }
  }

  // Max timer — force-pick for anyone still pending
  if (dialog.timer >= maxTimer) {
    for (const entry of dialog.entries) {
      if (entry.choice === null) {
        entry.choice = randomPickUpgrade(entry.offers, state);
      }
    }
  }

  return dialog.entries.every((entry) => entry.choice !== null);
}

/** Apply all picked upgrades to player state. */
export function applyUpgradePicks(
  state: GameState,
  dialog: UpgradePickDialogState,
): void {
  let secondWind = false;
  let clearTheField = false;
  for (const entry of dialog.entries) {
    if (entry.choice === null) continue;
    const player = state.players[entry.playerId];
    if (!player) continue;
    player.upgrades.set(entry.choice, 1);
    if (entry.choice === UID.SECOND_WIND) secondWind = true;
    if (entry.choice === UID.CLEAR_THE_FIELD) clearTheField = true;
  }
  if (secondWind) {
    for (let idx = 0; idx < state.towerAlive.length; idx++) {
      state.towerAlive[idx] = true;
    }
    state.towerPendingRevive.clear();
  }
  if (clearTheField) {
    state.grunts.length = 0;
  }
}

/** Pick a random offer using the synced RNG. */
function randomPickUpgrade(
  offers: readonly [UpgradeId, UpgradeId, UpgradeId],
  state?: GameState,
): UpgradeId {
  if (!state) return offers[0];
  return offers[Math.floor(state.rng.next() * offers.length)]!;
}

/** AI-aware pick: contextual upgrade selection based on game state. */
function aiPickUpgrade(
  offers: readonly [UpgradeId, UpgradeId, UpgradeId],
  state: GameState,
  playerId: ValidPlayerSlot,
): UpgradeId {
  const hasDeadTowers = playerHasDeadTowers(state, playerId);
  if (hasDeadTowers && offers.includes(UID.SECOND_WIND as UpgradeId)) {
    return UID.SECOND_WIND as UpgradeId;
  }
  const hasGruntsInZone = playerHasGruntsInZone(state, playerId);
  if (hasGruntsInZone && offers.includes(UID.CLEAR_THE_FIELD as UpgradeId)) {
    return UID.CLEAR_THE_FIELD as UpgradeId;
  }
  const hasPits = playerHasBurningPitsInZone(state, playerId);
  if (hasPits && offers.includes(UID.FOUNDATIONS as UpgradeId)) {
    return UID.FOUNDATIONS as UpgradeId;
  }
  const largeTerritory =
    playerTerritoryRatio(state, playerId) >= SMALL_PIECES_TERRITORY_RATIO;
  if (largeTerritory && offers.includes(UID.SMALL_PIECES as UpgradeId)) {
    return UID.SMALL_PIECES as UpgradeId;
  }
  // Exclude contextual upgrades when conditions aren't met
  const excluded = new Set<UpgradeId>();
  if (!hasDeadTowers) excluded.add(UID.SECOND_WIND as UpgradeId);
  if (!hasGruntsInZone) excluded.add(UID.CLEAR_THE_FIELD as UpgradeId);
  if (!hasPits) excluded.add(UID.FOUNDATIONS as UpgradeId);
  if (!largeTerritory) excluded.add(UID.SMALL_PIECES as UpgradeId);
  const viable = offers.filter((id) => !excluded.has(id));
  const pool = viable.length > 0 ? viable : offers;
  return pool[Math.floor(state.rng.next() * pool.length)]!;
}

function playerTerritoryRatio(
  state: GameState,
  playerId: ValidPlayerSlot,
): number {
  const player = state.players[playerId];
  if (!player?.homeTower || player.interior.size === 0) return 0;
  const zone = player.homeTower.zone;
  let zoneGrassCount = 0;
  for (let row = 0; row < GRID_ROWS; row++) {
    for (let col = 0; col < GRID_COLS; col++) {
      if (
        isGrass(state.map.tiles, row, col) &&
        state.map.zones[row]![col] === zone
      ) {
        zoneGrassCount++;
      }
    }
  }
  return zoneGrassCount > 0 ? player.interior.size / zoneGrassCount : 0;
}

function playerHasDeadTowers(
  state: GameState,
  playerId: ValidPlayerSlot,
): boolean {
  const player = state.players[playerId];
  if (!player) return false;
  return player.ownedTowers.some((tower) => !state.towerAlive[tower.index]);
}

function playerHasGruntsInZone(
  state: GameState,
  playerId: ValidPlayerSlot,
): boolean {
  const player = state.players[playerId];
  if (!player?.homeTower) return false;
  const zone = player.homeTower.zone;
  return state.grunts.some(
    (grunt) => state.map.zones[grunt.row]?.[grunt.col] === zone,
  );
}

function playerHasBurningPitsInZone(
  state: GameState,
  playerId: ValidPlayerSlot,
): boolean {
  const player = state.players[playerId];
  if (!player?.homeTower) return false;
  const zone = player.homeTower.zone;
  return state.burningPits.some(
    (pit) => state.map.zones[pit.row]?.[pit.col] === zone,
  );
}
